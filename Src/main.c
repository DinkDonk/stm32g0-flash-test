/**
* @brief Function implementing the flashTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartFlashTask */
void StartFlashTask(void const * argument)
{
  /* USER CODE BEGIN StartFlashTask */
  bool buttonPressed = false;
  uint32_t address = FLASH_BASE + (65 * FLASH_PAGE_SIZE); // Page 65
  uint8_t buffer[256] = {0};

  /* Infinite loop */
  for(;;)
  {
  	if (!LL_GPIO_IsInputPinSet(BUTTON_GPIO_Port, BUTTON_Pin) && buttonPressed == false)
	{
		buttonPressed = true;
		uint8_t data[16] = "Hello, World!!!!";
		uint32_t status = flash_write(address, data, 16);
		flash_read(address, buffer, 16);
		uint32_t length = sprintf((char *)buffer + 15, " Status: %lu\r\n", status);
		DEBUG_Print((char *)buffer, length + 15);

		if (status != 0)
		{
			Error_Handler();
		}
	}

  	if (LL_GPIO_IsInputPinSet(BUTTON_GPIO_Port, BUTTON_Pin) && buttonPressed == true)
	{
		buttonPressed = false;
	}

    osDelay(1);
  }
  /* USER CODE END StartFlashTask */
}

